#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	int	index;

	index = 0;
	if (lst == NULL)
		return (NULL);
	while (lst)
	{
		if (lst->next == NULL)
			return (lst);
		lst = lst->next;
	}
	return (lst);
}
